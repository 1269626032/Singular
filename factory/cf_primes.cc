int cf_getPrime( int i )
{
    ASSERT( i >= 0 && i < NUMPRIMES, "index to primes too high" );
    if ( cf_glob_switches.isOn( SW_FAC_USE_BIG_PRIMES ) ) {
	if ( i >= NUMSMALLPRIMES )
	    return bigprimes[NUMBIGPRIMES-i+NUMSMALLPRIMES];
	else
	    return primes[NUMSMALLPRIMES-i];
    }
    else {
	if ( i >= NUMSMALLPRIMES )
	    return bigprimes[i-NUMSMALLPRIMES];
	else
	    return primes[i];
    }
}

